    private CursoAcademico getCampos(){
        CursoAcademico cursoAca = new CursoAcademico();
        cursoAca.setYearinicio(Integer.parseInt(txtFin.getText()));
        cursoAca.setYearfin(Integer.parseInt(txtInicio.getText()));
        cursoAca.setDescripcion(txtDesc.getText());
        
        return cursoAca;
    }
    
    public void setCampos(){
        CursoAcademico cursoAca = new CursoAcademico();
        int filaSelec=jtCursos.getSelectedRow();
        cursoAca.setId((int) jtCursos.getValueAt(filaSelec, 0));
        cursoAca.setYearinicio((int) jtCursos.getValueAt(filaSelec, 1));
        cursoAca.setYearfin((int) jtCursos.getValueAt(filaSelec, 2));
        cursoAca.setDescripcion((String) jtCursos.getValueAt(filaSelec, 3));
    }
    
    
